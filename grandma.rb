def speak_to_grandma(phrase)
  if phrase == "I LOVE YOU GRANDMA!"
    'I LOVE YOU TOO PUMPKIN!'
  elsif phrase == "Hi Nana, how are you?"
    'HUH?! SPEAK UP, SONNY!'
  elsif phrase == "Hi!"
    'HUH?! SPEAK UP, SONNY!'
  elsif phrase == "WHAT DID YOU EAT TODAY?"
    'NO, NOT SINCE 1938!'
  elsif phrase == "WHAT?"
    'NO, NOT SINCE 1938!'
end
end